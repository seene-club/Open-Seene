#import "DeviceRecognition.h"
#include <sys/sysctl.h>
#import <UIKit/UIKit.h>

@implementation DeviceRecognition

-(NSString *) platform {
    size_t size;
    sysctlbyname("hw.machine", NULL, &size, NULL, 0);
    char *machine = malloc(size);
    sysctlbyname("hw.machine", machine, &size, NULL, 0);
    NSString *platform = [NSString stringWithCString:machine encoding:[NSString defaultCStringEncoding]];
    free(machine);
    return platform;
}

-(NSString *) type {
    NSString *deviceType = [UIDevice currentDevice].model;
    if ([deviceType rangeOfString:@"iPad"].location != NSNotFound) return @"iPad";
    if ([deviceType rangeOfString:@"iPhone"].location != NSNotFound) return @"iPhone";
    return nil;
}

-(float) screenWidth {
    CGRect screenRect = [[UIScreen mainScreen] bounds];
    return screenRect.size.width;
}

-(float) screenHeight {
    CGRect screenRect = [[UIScreen mainScreen] bounds];
    return screenRect.size.height;
}

